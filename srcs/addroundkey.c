#include <aes.h>

void	addroundkey(int round)
{
	int i;
	int	j;

	for(i = 0; i < 4; i++)
	{
		for(j = 0; j < 4; j++)
			g_state[j][i] ^= g_round_keys[(round * NB * 4) + (i * NB) + j];
	}
}
